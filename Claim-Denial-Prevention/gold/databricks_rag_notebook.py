# MAGIC ## Cell 3 — Recursive Character Text Splitter
# MAGIC
# MAGIC **Why Recursive?** Our policy documents have a clear hierarchy:
# MAGIC - `SECTION 1` → `POLICY 1.1` → paragraph → sentence → word
# MAGIC
# MAGIC The recursive splitter respects this by trying separators in order:
# MAGIC `\n\n` (section break) → `\n` (line break) → `. ` (sentence) → ` ` (word)
# MAGIC
# MAGIC This guarantees that `POLICY 1.1 — Maximum Billable Amount...` stays in one chunk
# MAGIC instead of being cut mid-sentence by a fixed-size splitter.

# COMMAND ----------

def recursive_char_split(text: str, max_words: int, overlap: int) -> list:
    """
    Recursively splits text into chunks by trying structural separators in order.
    Each chunk tries to stay within `max_words`, with `overlap` words shared
    between consecutive chunks to avoid losing boundary context.
    
    Separator priority (most structural → least structural):
      1. \\n\\n  — section/paragraph break
      2. \\n    — line break
      3. '. '   — sentence boundary
      4. ' '    — word boundary (final fallback)
    """
    SEPARATORS = ["\n\n", "\n", ". ", " "]

    def _split(segment: str, sep_index: int) -> list:
        # Base case: if no more structural separators, split by raw word count
        if sep_index >= len(SEPARATORS):
            words = segment.split()
            result = []
            i = 0
            while i < len(words):
                result.append(" ".join(words[i : i + max_words]))
                i += max_words - overlap
            return result

        sep = SEPARATORS[sep_index]
        parts = [p.strip() for p in segment.split(sep) if p.strip()]

        # Merge parts greedily until a chunk would exceed max_words
        chunks = []
        buffer = ""
        for part in parts:
            candidate = (buffer + " " + part).strip() if buffer else part
            if len(candidate.split()) <= max_words:
                buffer = candidate
            else:
                if buffer:
                    chunks.append(buffer)
                    # Carry overlap forward into next chunk
                    overlap_text = " ".join(buffer.split()[-overlap:]) if overlap else ""
                    buffer = (overlap_text + " " + part).strip()
                else:
                    # Part alone is too large — recurse deeper with next separator
                    sub = _split(part, sep_index + 1)
                    chunks.extend(sub)
                    buffer = ""
        if buffer:
            chunks.append(buffer)
        return chunks

    # Normalise whitespace before splitting
    text = re.sub(r" {2,}", " ", text.strip())
    return _split(text, 0)


def load_and_chunk_documents(docs_path: str, max_words: int, overlap: int) -> list:
    """
    Loads all .txt files from docs_path, applies recursive character splitting,
    and returns a flat list of chunk dicts: {chunk_id, source_doc, text}.
    """
    policy_files = sorted([f for f in os.listdir(docs_path) if f.endswith(".txt")])
    if not policy_files:
        raise FileNotFoundError(f"No .txt files found at: {docs_path}")

    all_chunks = []
    for filename in policy_files:
        with open(os.path.join(docs_path, filename), "r", encoding="utf-8") as fh:
            raw_text = fh.read()

        chunks = recursive_char_split(raw_text, max_words, overlap)
        for i, chunk_text in enumerate(chunks):
            all_chunks.append({
                "chunk_id"  : f"{filename}__chunk_{i}",
                "source_doc": filename,
                "text"      : chunk_text,
            })
        print(f"  [{filename}] → {len(chunks)} chunks")

    print(f"\nTotal chunks across all 3 policy documents: {len(all_chunks)}")
    return all_chunks


