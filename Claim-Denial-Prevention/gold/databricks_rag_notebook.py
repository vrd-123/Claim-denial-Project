# MAGIC %md
# MAGIC ## Cell 6 — ChromaDB Query Function
# MAGIC
# MAGIC **Automated Query Flow (no user input required):**
# MAGIC
# MAGIC ```
# MAGIC Raw Claim Data (structured)
# MAGIC     ↓
# MAGIC ML Model → SHAP Values → Reason Mapper
# MAGIC     ↓
# MAGIC "The medical diagnosis code is missing or invalid."   ← AUTO-GENERATED QUERY
# MAGIC     ↓
# MAGIC ChromaDB (cosine similarity search)
# MAGIC     ↓
# MAGIC "POLICY 2.1 — All claims MUST contain a valid ICD-10-CM diagnosis code..."
# MAGIC ```
# MAGIC
# MAGIC One query is issued **per SHAP reason** (3 queries per denied claim).
# MAGIC Each query retrieves K=2 policy chunks, ensuring every reason
# MAGIC gets its own directly relevant supporting policy text.

# COMMAND ----------

def query_policy(reason_text: str, top_k: int = TOP_K) -> list:
    """
    Queries ChromaDB with a SHAP-generated denial reason sentence.
    Returns top_k most semantically similar policy chunks.
    
    The query IS the SHAP reason sentence — fully auto-generated by the
    pipeline. No user writes this text manually.
    
    Returns:
        List of dicts: [{policy_text, source_doc, similarity_score}, ...]
    """
    if not reason_text or reason_text.strip() == "":
        return []

    results = collection.query(
        query_texts = [reason_text],
        n_results   = top_k,
        include     = ["documents", "metadatas", "distances"],
    )

    output = []
    for doc, meta, dist in zip(
        results["documents"][0],
        results["metadatas"][0],
        results["distances"][0],
    ):
        # ChromaDB cosine distance → similarity score (0=identical, 2=opposite)
        # Convert: similarity = 1 - (distance / 2) to get a 0–1 score
        similarity = round(1 - dist / 2, 4)
        output.append({
            "policy_text"     : doc,
            "source_doc"      : meta.get("source_doc", "unknown"),
            "similarity_score": similarity,
        })
    return output


