# MAGIC ## Cell 7 — Build Combined Explanation Rows
# MAGIC
# MAGIC **For each DENIED claim:**
# MAGIC - Reason 1 text → ChromaDB query → Best matching policy passage
# MAGIC - Reason 2 text → ChromaDB query → Best matching policy passage
# MAGIC - Reason 3 text → ChromaDB query → Best matching policy passage
# MAGIC - All assembled into a clean `full_explanation` string
