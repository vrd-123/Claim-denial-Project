PROCESSED_AT = datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S")
output_rows  = []

print(f"Processing {len(explanations_pd):,} claims through RAG pipeline...")

for _, row in explanations_pd.iterrows():

    base = {
        "claim_id"          : row["claim_id"],
        "predicted_status"  : row["predicted_status"],
        "denial_probability": row["denial_probability"],
        "processed_at"      : PROCESSED_AT,
    }

    if row["predicted_status"] == "DENIED":

        reason_texts = [
            str(row.get("reason_1_text", "")),
            str(row.get("reason_2_text", "")),
            str(row.get("reason_3_text", "")),
        ]

        # Query ChromaDB independently for each of the 3 SHAP reasons
        policy_hits = []
        for r_text in reason_texts:
            hits = query_policy(r_text, top_k=1)  # Best policy per reason
            policy_hits.append(hits[0] if hits else {
                "policy_text": "No matching policy found.",
                "source_doc" : "N/A",
                "similarity_score": 0.0,
            })

        # Populate structured columns
        for i in range(3):
            base[f"reason_{i+1}_text"]          = reason_texts[i]
            base[f"policy_{i+1}_text"]          = policy_hits[i]["policy_text"]
            base[f"policy_{i+1}_source"]        = policy_hits[i]["source_doc"]
            base[f"policy_{i+1}_similarity"]    = policy_hits[i]["similarity_score"]

        # Build the human-readable full explanation (matches the agreed response format)
        explanation_lines = [
            f"CLAIM ID: {row['claim_id']}",
            f"STATUS  : DENIED  (Denial Probability: {row['denial_probability']*100:.1f}%)",
            "",
        ]
        for i in range(3):
            explanation_lines += [
                f"REASON {i+1} (From ML Model + SHAP):",
                f"  {reason_texts[i]}",
                f"SUPPORTING POLICY (Source: {policy_hits[i]['source_doc']}):",
                f"  \"{policy_hits[i]['policy_text'][:350]}...\"",
                "",
            ]
        base["full_explanation"] = "\n".join(explanation_lines)

    else:  # APPROVED
        for i in range(3):
            base[f"reason_{i+1}_text"]       = row.get(f"reason_{i+1}_text", "N/A")
            base[f"policy_{i+1}_text"]       = "N/A — Claim meets all adjudication standards."
            base[f"policy_{i+1}_source"]     = "N/A"
            base[f"policy_{i+1}_similarity"] = 0.0

        base["full_explanation"] = (
            f"CLAIM ID: {row['claim_id']}\n"
            f"STATUS  : APPROVED  (Denial Probability: {row['denial_probability']*100:.1f}%)\n"
            f"No policy violations detected. Claim meets all adjudication standards."
        )

    output_rows.append(base)


